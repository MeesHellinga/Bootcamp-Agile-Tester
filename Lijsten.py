merk = ['ah','coop','plus','spar']
print(merk[0])		# ah
print(merk[-1])		# spar
print(merk[1:3])	# ['coop','plus']
print(merk[2:])		# ['plus','spar']
print(merk[:3])		# ['ah','coop','plus']
print(merk[1:-2])	# ['coop']

